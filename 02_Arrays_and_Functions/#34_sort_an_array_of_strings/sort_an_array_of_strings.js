
let fruits = [
    "apple",
    "banana",
    "orange",
]

// khai báo mãng

for (let fruit of fruits) {
    console.log(fruit)
}
//  dùng vòng lặp for of

// sort: sắp xếp theo chữ cái

fruits = fruits.sort()
console.log(fruits)

//  sắp xếp mãng trái cây, đảo ngược
fruits = fruits.sort.reverse();
console.log(fruits)